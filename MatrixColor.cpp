uint8_t SmartMatrix::colorCorrection8bit(uint8_t inputcolor) {
    switch (SmartMatrix::_ccmode) {
    case cc24:
        return lightPowerMap8bit[inputcolor];

    case cc12:
        return lightPowerMap4bit[inputcolor] << 4;

    case ccNone:
        return inputcolor;
    }

    return 0;
}
