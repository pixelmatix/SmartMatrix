#include "SmartMatrix.h"

void SmartMatrix::copyRgb24(rgb24 & dst, const rgb24 &src) {
    dst.red = src.red;
    dst.green = src.green;
    dst.blue = src.blue;
}

colorCorrectionModes SmartMatrix::_ccmode = cc24;

void SmartMatrix::setColorCorrection(colorCorrectionModes mode) {
    _ccmode = mode;
}

